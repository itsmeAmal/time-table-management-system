package com.itpm.model;

public class StudentGroup {

    private int studentId;
    private String groupNo;
    private String subGroupNo;
    private int groupId;
    private int subGroupId;
    private String academicYearAndSem;
    private String program;

    /**
     * @return the studentId
     */
    public int getStudentId() {
        return studentId;
    }

    /**
     * @param studentId the studentId to set
     */
    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    /**
     * @return the groupNo
     */
    public String getGroupNo() {
        return groupNo;
    }

    /**
     * @param groupNo the groupNo to set
     */
    public void setGroupNo(String groupNo) {
        this.groupNo = groupNo;
    }

    /**
     * @return the subGroupNo
     */
    public String getSubGroupNo() {
        return subGroupNo;
    }

    /**
     * @param subGroupNo the subGroupNo to set
     */
    public void setSubGroupNo(String subGroupNo) {
        this.subGroupNo = subGroupNo;
    }

    /**
     * @return the groupId
     */
    public int getGroupId() {
        return groupId;
    }

    /**
     * @param groupId the groupId to set
     */
    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    /**
     * @return the subGroupId
     */
    public int getSubGroupId() {
        return subGroupId;
    }

    /**
     * @param subGroupId the subGroupId to set
     */
    public void setSubGroupId(int subGroupId) {
        this.subGroupId = subGroupId;
    }

    /**
     * @return the academicYearAndSem
     */
    public String getAcademicYearAndSem() {
        return academicYearAndSem;
    }

    /**
     * @param academicYearAndSem the academicYearAndSem to set
     */
    public void setAcademicYearAndSem(String academicYearAndSem) {
        this.academicYearAndSem = academicYearAndSem;
    }

    /**
     * @return the program
     */
    public String getProgram() {
        return program;
    }

    /**
     * @param program the program to set
     */
    public void setProgram(String program) {
        this.program = program;
    }

}
